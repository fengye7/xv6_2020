uint64
sys_exit(void)
{
  int n;
  if(argint(0, &n) < 0)
    return -1;
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  if(argaddr(0, &p) < 0)
    return -1;
  return wait(p);
}

uint64
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  //添加backtrace的调用
  backtrace();
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

uint64 
sys_sigreturn(void){
  struct proc* p=myproc();
  //恢复现场
  memmove(p->trapframe,&(p->resume),sizeof(struct trapframe));
  //清零计时器
  p->curticks=0;
  return 0;
}

uint64 
sys_sigalarm(void){
  //保存ticks和handler到proc结构中
  int ticks;
  if(argint(0, &ticks) < 0){
    return -1;
  }
  uint64 handler;
  if(argaddr(1,&handler)<0){
    return -1;
  }
  struct proc* p = myproc();
  p->ticks=ticks;
  p->handler=handler;
  // 重置过去的时钟数
  p->curticks = 0;

  return 0;
}